// Tipos de datos basicos en C++

#include <iostream>

using namespace std;

int main()
{
    // Tipos de datos
    int entero = 10;
    float flotante = 10.45;
    // double es similar al float solo q almacena datos mas grandes
    double mayor = 16.3456;
    char letra = 'a';

    cout << "Tipo de dato Entero: " << entero << endl;
    cout << "Tipo de dato Flotante: " << flotante << endl;
    cout << "Tipo de dato Double: " << mayor << endl;
    cout << "Tipo de dato Char: " << letra << endl;

    return 0;
}